# Build X (intercept + 4 ln distances) and Y (ln actual price), in METERS
Y = np.array([math.log(r[0]) for r in raw])
X_rows = []
for r in raw:
    d_pend = min(r[1],r[2],r[3])
    d_pt   = min(r[4],r[5])
    d_kes  = min(r[6],r[7])
    d_kom  = min(r[8],r[9],r[10],r[11])
    X_rows.append([1.0, math.log(d_kom), math.log(d_pend), math.log(d_kes), math.log(d_pt)])

X = np.array(X_rows)

# OLS closed-form: β = (X'X)^{-1} X'Y
beta = np.linalg.lstsq(X, Y, rcond=None)[0]

Y_hat   = X @ beta
resid   = Y - Y_hat
n, k    = 50, 4
SS_res  = float((resid**2).sum())
SS_tot  = float(((Y - Y.mean())**2).sum())
SS_reg  = SS_tot - SS_res
R2      = 1 - SS_res/SS_tot
adjR2   = 1 - (SS_res/(n-k-1)) / (SS_tot/(n-1))
F       = (SS_reg/k) / (SS_res/(n-k-1))

